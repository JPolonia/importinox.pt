<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">8.2 Até que a respectiva propriedade seja transferida assiste à Importinox o direito de reaver quaisquer mercadorias na posse ou sob o controlo do cliente comprador.</span>
</p>
<p align="justify" style="MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<b>
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9. Reclamações </span>
</b>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.1 O Cliente obriga-se a proceder ao exame de todas as mercadorias no acto da entrega, pelo que a Importinox não aceitará quaisquer reclamações posteriores. </span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.2 Se, por qualquer motivo justificado, as mercadorias não tiverem sido examinadas no momento da entrega, as eventuais reclamações do Cliente só poderão ser apresentadas no prazo de oito dias após a entrega das mercadorias.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.3 As reclamações relativas a defeitos que não podiam ter sido razoavelmente detectados pelo Cliente, ainda que este tivesse agido com a diligência devida, só poderão ser apresentadas à Importinox no prazo de dois dias a contar da data em que tenha sido detectado o defeito e dentro do prazo de um mês a contar do fornecimento das mercadorias. </span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.4 A Importinox, a verificar-se uma reclamação justificada por parte do Cliente, reserva-se o direito de optar ou pela substituição das mercadorias defeituosas, ou pelo reembolso, total ou parcial, do valor facturado ao Cliente. Em qualquer dos casos, não poderá ser reclamado pelo Cliente qualquer tipo de indemnização e/ou compensação a seu favor, ou de terceiros.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.5 Qualquer reclamação deve ser apresentada por escrito de recepção comprovada, sob pena de não ser considerada.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.6 No caso de uma reclamação tardia, a Importinox não fica obrigada a verificá-la, nem a efectuar a substituição das mercadorias defeituosas ou o reembolso do preço respectivo. </span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.7 Não serão aceites devoluções ou reclamações de mercadorias danificadas em trânsito ou em operações de carga ou descarga. </span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.8 As mercadorias apenas poderão ser consideradas defeituosas se não estiverem de acordo com as especificações do Cliente ou das normas DIN / ISO.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">9.9 A Importinox não poderá ser responsabilizada pelo não cumprimento ou pelo atraso no cumprimento das obrigações por ela assumidas, se o incumprimento ou o atraso tiverem resultado de factos ou circunstâncias fora do seu controlo razoável ou se forem da responsabilidade de terceiro, como sejam greves, conflitos, acidentes, inundações ou condições climatéricas extremas, e quaisquer rupturas de fabrico, de fornecimento ou de transporte.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<b>
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10. Disposições finais</span>
</b>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.1 As mercadorias viajam sempre por conta e risco do Cliente, a não ser que a Importinox o tenha assumido expressamente.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-BOTTOM: 0cm">
<span style="COLOR: #000000; FONT-SIZE: 16px">
<br>
</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.2 A nota de encomenda, as condições gerais de venda da Importinox e quaisquer condições e ou alterações acordadas por escrito constituem o contrato integral estabelecido entre as partes, substituindo todas as negociações, compromissos e ou acordos anteriores, escritos ou verbais, relativamente às mercadorias abrangidas pelo contrato. </span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.3 À excepção daquilo que se encontre estabelecido nas condições gerais de venda, ou que haja sido acordado por escrito, a Importinox não poderá ser responsabilizada pelo cliente, ou por terceiros adquirentes, por quaisquer perdas ou danos directos ou indirectos.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.4 As condições gerais de venda da Importinox prevalecem sobre quaisquer condições de compra do Cliente.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.5 O lugar do cumprimento pelo cliente das respectivas obrigações é o da sede ou escritórios da Importinox.</span>
</p>
<p align="justify" style="LINE-HEIGHT: 200%; MARGIN-TOP: 0.18cm; MARGIN-BOTTOM: 0.42cm">
<span style="FONT-FAMILY: Arial, sans-serif; COLOR: #000000; FONT-SIZE: 16px">10.6 Para apreciar e decidir quaisquer divergências, controvérsias, ou reclamações surgidas na sequência do contrato ou da encomenda ou com relação às mercadorias vendidas ou à cobrança do respectivo preço, será competente e fica convencionado o Tribunal Judicial da Maia, renunciando as partes a qualquer outro. </span>
